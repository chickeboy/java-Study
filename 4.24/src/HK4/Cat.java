package HK4;

public class Cat {
	String name;
	int age;
	String color;
	public void blackcat() {
		name = "小黑";
		age = 2;
		color = "黑色";
		System.out.println(name+","+age+","+color);
	}
	public void whitecat() {
		name = "小白";
		age = 5;
		color = "白色";
		System.out.println(name+","+age+","+color);
	}
}
