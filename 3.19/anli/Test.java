//注释：不执行的东西，只是用来作为笔记
public class Test{//代码块
//关键字:具有特殊含义的系统自带的英文单词
//public 共有的 class 类
	//遇到{下一行代码必须缩进一个tab键的距离
	public static void main(String[] args){
	//main方法，固定写法，不会变，表示代码的入口，代码从这里开始执行
	System.out.println("hello world");//;表示一句代码的终结
	//系统向外输出内容，并且换行
	System.out.println(100 + 32);//整数
	System.out.println("100" + 32 + 3);//"10032" + 3
	//字符串和任何数据类型做+操作最后结果都是字符串
	}
}
